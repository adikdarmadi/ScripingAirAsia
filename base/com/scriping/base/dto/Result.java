package com.scriping.base.dto;

public class Result{

	private String jadwalBerangkat;
	private String lowFare;
	private String hiFlyer;
	
	
	public String getJadwalBerangkat() {
		return jadwalBerangkat;
	}
	public void setJadwalBerangkat(String jadwalBerangkat) {
		this.jadwalBerangkat = jadwalBerangkat;
	}
	public String getLowFare() {
		return lowFare;
	}
	public void setLowFare(String lowFare) {
		this.lowFare = lowFare;
	}
	public String getHiFlyer() {
		return hiFlyer;
	}
	public void setHiFlyer(String hiFlyer) {
		this.hiFlyer = hiFlyer;
	}

	
	
}